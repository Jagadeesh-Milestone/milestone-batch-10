#type conversion:
#converting one data type to another data type is known as type conversion.
a=10
b=float(a)
print(b)

c=complex(a)
print(c)

d=bool(a)
print(d)

e=str(a)
print(e)
print(type(e))

#list:
l=[10,20,30,40]
f=tuple(l)
print(f)

g=set(l)
print(g)

h=frozenset(l)
print(h)

i=bytes(l)
print(i)

j=bytearray(l)
print(j)