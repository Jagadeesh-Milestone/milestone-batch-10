#binary numbers:
#starts with 0b.
#base value is 2
#possible values are 0,1.
a=10
print(a)
print(bin(a))

b=0b1010
print(b)

#hexa decimal numbers:
#starts with 0x
#base value is 16
#possible values are 0-9,a-f.
c=10
print(hex(c))

d=0xa
print(d)

#octal numbers:
#starts with 0o
#base value is 8
#possible values are 0-7.
e=10
print(oct(e))

f=0o12
print(f)