#python objects:
#in oops the object is an entity that has state and behaviour.
#it may be a realworld entities like pen,pencil,keyboard,mouse.
#in python everything is a object like integers,float values,list,tuple,
#dictionary etc,

#properties of objects:
#state:
# it is represented by the attributes of an object.it also reflects the
#properties of an object.
#ex--state is considered as color,size etc of an object.

#behaviour:
#it is represented by the methods of an object.it reflects the response of
#one object to other object.
#ex--the behavour can be considered as how we are using the mouse.

#identity:
#it gives unique name to an object and enables one object interact with other object

#ex--the identity can be considered as the name of an object,,mouse.